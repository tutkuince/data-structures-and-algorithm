package io.tince._03bigonotation;

public class ProportionToNumberOfItems {
    // O(n) -> means in proportion to the number of items.

    // Do not change the main method below
    public static void main(String[] args) {
        printItems(10);
    }

    private static void printItems(int n) {
        for (int i = 0; i < n; i++) {
            System.out.println(i);
        }
    }
}
